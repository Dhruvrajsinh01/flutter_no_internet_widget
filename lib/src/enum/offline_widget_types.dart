/// List of all Offline widget types
enum OfflineWidgetType {
  ///Display a snackbar when there is no internet
  snackbar,

  ///Display a popup when there is no internet
  popup,

  ///Display a full screen widget when there is no internet
  fullscreen,

  ///Display a bottomsheet when there is no internet
  bottomsheet,

  ///Ignore when there is no internet
  none,
}
